    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        Customer customer = new Customer(updateIdText.getText(), updateNameText.getText(), updateNicText.getText(), updateAddressText.getText(), Integer.parseInt(updateContactText.getText()));
        try {
            boolean updateCustomer = CustomerController.updateCustomer(customer);
            if (updateCustomer) {
                
                JOptionPane.showMessageDialog(null, "Customer Update !");
                updateIdText.setText("");
                updateNameText.setText("");
                updateNicText.setText("");
                updateAddressText.setText("");
                updateContactText.setText("");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Customer Not Update !");
        }

    }//GEN-LAST:event_updateButtonActionPerformed

