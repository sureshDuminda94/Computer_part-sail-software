    private void nameTextKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nameTextKeyTyped
        //defaultTableModel.setRowCount(0);
        Validation.inputCharaterOnly(nameText);
        String customerName = nameText.getText();
        int size = customerName.toCharArray().length;
        
        try {
            if (null == customerList) {
                customerList = CustomerController.customerList();
            }
            for (Customer customer : customerList) {
                String name = customer.getName();
                String substring = name.substring(0, size);
                if (customerName.equals(substring)) {
                    Object data[] = {customer.getCid(), customer.getName(), customer.getNic(), customer.getAddress(), customer.getContact()};
                    defaultTableModel.setRowCount(0);
                    defaultTableModel.addRow(data);
                }
                
            }
            
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Customer Not Found !");
        }

    }//GEN-LAST:event_nameTextKeyTyped

