    public static void setOrderItemViewForm(ArrayList<Item> itemList) {
        final NewOrderItemViewForm itemViewForm = new NewOrderItemViewForm(itemList);
        loadPanel.removeAll();
        loadPanel.repaint();
        loadPanel.revalidate();
        loadPanel.add(itemViewForm);
        new Thread() {
            public void run() {
                for (int i = -10; i < 1; i++) {
                    try {
                        Thread.sleep(6);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(ImageForm.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    itemViewForm.setLocation(i, 0);
                }
            }
        }.start();
    }

