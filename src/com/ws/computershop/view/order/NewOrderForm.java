    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        ArrayList<Item> itemList = itemSelectForm.getItemList();
        if (itemList.size() > 0) {
            double orderAmount = 0;
            ArrayList<OrderDetail> orderDetails = new ArrayList<>();

            if (null != itemList) {
                for (Item item : itemList) {
                    orderDetails.add(new OrderDetail(orderIdText.getText(), item.getCode(), item.getQty(), item.getPrice()));
                    orderAmount += (item.getQty() * item.getPrice());
                }
            }

            Order order = new Order(orderIdText.getText(), customerIdText.getText(), dateText.getText(), orderAmount);

            try {
                boolean addNewOrder = OrderController.addNewOrder(order, orderDetails, itemList);
                if (addNewOrder) {
                    JOptionPane.showMessageDialog(null, "Save Order");
                    this.setOrderId();
                } else {
                    JOptionPane.showMessageDialog(null, "Not Save Order !");
                }
            } catch (ClassNotFoundException | SQLException ex) {
                JOptionPane.showMessageDialog(null, "Not Save Order !");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please Select Item !");
        }


    }//GEN-LAST:event_jButton3ActionPerformed

